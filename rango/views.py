from datetime import datetime

def visitor_cookie_handler(request, response):
    # Get the number of visits to the site.
    # If the cookie exists, cast it to an int.
    # If it doesn't exist, default to 1.
    visits = int(request.COOKIES.get('visits', '1'))

    last_visit_cookie = request.COOKIES.get('last_visit', str(datetime.now()))
    last_visit_time = datetime.strptime(last_visit_cookie[:-7], '%Y-%m-%d %H:%M:%S')

    # If it's been more than a day since the last visit...
    if (datetime.now() - last_visit_time).days > 0:
        visits = visits + 1
        # Update the last visit cookie now that we have updated the count
        response.set_cookie('last_visit', str(datetime.now()))
    else:
        # Set the last visit cookie
        response.set_cookie('last_visit', last_visit_cookie)

    # Update/set the visits cookie
    response.set_cookie('visits', visits)



def index(request):
    # Top 5 categories by likes
    category_list = Category.objects.order_by('-likes')[:5]

    # Top 5 pages by views
    page_list = Page.objects.order_by('-views')[:5]

    context_dict = {}
    context_dict['boldmessage'] = 'Crunchy, creamy, cookie, candy, cupcake!'
    context_dict['categories'] = category_list
    context_dict['pages'] = page_list

    # Add visits into the context so index.html can show it.
    context_dict['visits'] = int(request.COOKIES.get('visits', '1'))

    # Obtain our Response object early so we can add cookie information.
    response = render(request, 'rango/index.html', context=context_dict)

    # Call the helper function to handle the cookies
    visitor_cookie_handler(request, response)

    # Return response back to the user, updating any cookies that need changed.
    return response



